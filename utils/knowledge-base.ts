// Function to identify the category of a question based on keywords
export function identifyQuestionCategory(question: string): string[] {
  const normalizedQuestion = question.toLowerCase()
  const categories: string[] = []

  // Check for pronouns that might refer to Dr. Ali
  const containsRelevantPronouns = /\b(he|his|him|doctor|dr|asif|ali)\b/i.test(normalizedQuestion)

  // Check each category for keyword matches
  for (const [category, keywords] of Object.entries(keywordCategories)) {
    for (const keyword of keywords) {
      if (normalizedQuestion.includes(keyword.toLowerCase())) {
        categories.push(category)
        break
      }
    }
  }

  // If the question contains pronouns but no specific category was identified,
  // it might be a general question about Dr. Ali
  if (containsRelevantPronouns && categories.length === 0) {
    categories.push("general")
  }

  // If no category was identified, mark as unknown
  if (categories.length === 0) {
    categories.push("unknown")
  }

  return categories
}

